operation = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}

carry_over="y"
while carry_over=="y":
    result = operation[op](n1,n2)
    print(result)
    carry_over = input("Do you want to carry over the result to a new operation? (y/n): ").lower()
    if carry_over == "y":
        n1 = result
        op = input("Enter operation (+,-,*,/): ")
        n2 = int(input("Enter number 2: "))

